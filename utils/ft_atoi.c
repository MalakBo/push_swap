#include "push_swap.h"

int ft_atoi(char *s)
{
    int result;
    int sign;
    int i;
    
    i = 0;
    result = 0;
    sign = 1;
    while(s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
        i++;
    if(s[i] == '-')
    {
        sign = -1;
        i++;
    }
    else if(s[i] == '+')
        i++;
    while(s[i] && (s[i] >= '0' && s[i] <= '9'))
    {
        result = result * 10 + (s[i] - '0');
        i++;
    }
    return (result * sign);
}